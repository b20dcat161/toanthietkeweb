    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        if (!SessionUtils.checkLogin(request)) {
            SessionUtils.redirectToLogin(request, response);
            return;
        }
        List<Challenge> challenges = challengeDao.getChallenges();
        for (Challenge challenge : challenges) {
            System.out.println(challenge.getFilename());
        }
        request.setAttribute("challenges", challenges);
        request.getRequestDispatcher("challenges.jsp").forward(request, response);
    }

